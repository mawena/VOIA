                Le programme de vulgarisation de l’outil informatique en Afrique (VOIA) met à la disposition de ses apprenants une plateforme numérique pour les renseigner et faciliter leurs inscriptions à la formation en communication digitale. En s'inscrivant, l’utilisateur reconnaît qu'il est pleinement informé et qu'il est tenu par l'ensemble des dispositions de nos <strong style="cursor:pointer" id="cgu-show">conditions générales d'utilisation </strong> et de notre <strong style="cursor:pointer" id="pdc-show">politique de confidentialité</strong>. <br>
            </div>
            <div id="cgu-content" style="text-align: justify; display:none">
                <h1 style="text-align:center;">
                    CONTRAT DE SOUSCRIPTION AU PROGRAMME VOIA
                </h1>

                LE PRESENT CONTRAT DE SOUSCRIPTION EST CONCLU ENTRE : <br>

                1 L’Incubateur Social GHIYADA AFRICA dont le siège social est situé à Adidogomé la pampa, immeuble ancien FECECAV, (Lomé-TOGO)
                « L’émetteur » d’une part <br>
                Et <br>
                2 M. / Mme (vous) « Le souscripteur » d’autre part. <br>

                ETANT PREALABLEMENT RAPPELE QUE: <br>
                L'Emetteur envisage de procéder à un programme de formations en présentiel ou en ligne (virtuel) sur des modules du numérique à un coût forfaitaire de cinq mille (5000) FCFA ou dix mille (10 000) FCFA. Les modalités de formations (les "Modalités", le terme "Modalité" désignant un article des Modalités) figurent dans le présent Contrat. Si le contexte le permet et sauf stipulation contraire, les termes et expressions définis dans les Modalités auront la même signification dans le présent Contrat. <br>
                Le programme vise l’auto-employabilité des jeunes ; l’accessibilité, la disponibilité et la performance des profils dotés de compétences numériques de pointes et la compréhension des enjeux du numérique pour innover. <br>
                Ce mécanisme prend en compte la jeunesse estudiantine et lycéenne et encourage vivement les participations féminines. <br>

                IL A ETE ARRETE ET CONVENU CE QUI SUIT: <br>
                <h2>1. SOUSCRIPTION</h2> <br>
                Sous réserve des stipulations du présent Contrat, l'Emetteur s'engage à former le Souscripteur qui s'engage a priori à souscrire et payer le coût de formation tel qu’indiqué pour chacun d’eux au présent Contrat (l'"Engagement de Souscription") et le délai de l’accord (tel que défini à l'Article 2 ci-après). <br>

                <h2>
                    2. REGLEMENT
                </h2>
                Sous réserve que l’intégralité des obligations du Souscripteur à l’Article 5 soit satisfaite, le souscripteur versera à l’Emetteur le Prix de Souscription aux packs de formation disponibles soit : <br>
                • Mille francs (1.000) FCFA pour les frais d’inscription au Togo et deux mille cinq cent (2500) FCFA pour les participants hors du Togo <br>
                • Cinq mille (5.000) FCFA pour 8 heures de formation <br>
                • dix mille (10.000) FCFA pour 16 heures de formation
                juste après l’acceptation en fonds immédiatement disponibles par transfert d’argent par Flooz, Tmoney(en tenant compte des frais de retrait de la somme envoyée), par payement espèce au siège de l’incubateur GHIYADA AFRICA ou tout autre moyen de paiement légal (western union, money gram, wari, Juba express hors du Togo) à l'Emetteur. <br>

                <h2>
                    3. ENGAGEMENTS DE L’EMETTEUR
                </h2>

                L'Emetteur s'engage à l'égard du Souscripteur à: <br>
                a. Former le souscripteur dans les modules de la communication digitale à savoir : <br>
                • Comment créer et monétiser sa chaine youtube, <br>
                • Comment créer et monétiser sa page Facebook <br>
                • Faire du e-commerce avec son téléphone <br>
                • E-marketing <br>
                • Community management <br>
                • <strong> Entrepreneuriat numérique (créer, manager et lever des fonds pour son entreprise numérique)</strong> <br>
                • <strong>Montage vidéo avec son téléphone</strong> <br>

                Les deux derniers modules cités ci-dessus en gras sont uniquement accessibles qu’à la souscription du pack de dix mille (10 000) FCFA. <br>
                b. Décerner une attestation de fin de formation au Souscripteur (à condition qu’il suive intégralement les formations) <br>
                c. Equiper le Souscripteur en matériel de travail (un Smartphone) après que celui-ci ait concouru à la souscription de cinq (5) personnes au pack de dix mille (10 000) FCFA ou dix (10) personnes au pack de cinq mille (5000) FCFA. <br>
                d. Accorder une période de deux (2) mois à compter de la date de souscription au Souscripteur en vue d’atteindre le quota de personnes dont il doit encourager la souscription) au programme (soit cinq ou dix personnes selon le pack souscrit) <br>

                <h2>
                    4. DECLARATIONS ET GARANTIES DE L’EMETTEUR
                </h2>
                En contrepartie de l'engagement pris par le Souscripteur de souscrire et honorer chaque clause du présent accord, l'Emetteur déclare et garantit au Souscripteur que: <br>
                <strong>(A) Formations et équipement de travail</strong> <br>
                a. Les mesures nécessaires sont prises pour offrir une formation de qualité dans un cadre approprié (en virtuel ou présentiel selon le choix de l’intéressé) par une équipe dynamique et compétente de formateurs <br>

                b. Les modules sur la communication digitale, à savoir : <br>
                • Comment créer et monétiser sa chaine youtube, <br>
                • Comment créer et monétiser sa page Facebook <br>
                • Faire du e-commerce avec son téléphone <br>
                • E-marketing <br>
                • Community management <br>
                • Entrepreneuriat numérique (créer, manager et lever des fonds pour son entreprise numérique) <br>
                • Montage vidéo avec son téléphone, <br>
                sont soumis au choix du souscripteur suivant son pouvoir d’achat conformément à l’article 3.a <br>
                c. L’équipement de travail (Smartphone) serait gratuitement offert au souscripteur (en cas de validation des clauses du bonus) <br>

                <strong>(B) Modalités</strong> <br>
                Les obligations qui incombent à l'Emetteur au titre du présent Contrat, des Modalités et du Contrat de formation sont conformes à la loi du pays de son siège social, valables, lui sont opposables et sont susceptibles d'être mises en œuvre en justice. <br>

                a. L’acceptation du présent contrat de souscription n’est effective qu’après le paiement des frais d’inscription et de formation par transfert d’argent T.money ou Flooz, ou un versement en espèce au siège social de l’Emetteur sis à Adidogomé la pampa, ancien immeuble FECECAV (Lomé-TOGO), ou toute autre forme de paiement légal. <br>
                b. Les packs de souscription de cinq mille (5000) et dix mille (10.0000) FCFA offrent chacun au Souscripteur la garantie d’une formation d’un (1) mois dans les modules sur la communication digitale tels que : Comment créer et monétiser sa chaine youtube, Comment créer et monétiser sa page Facebook, Faire du e-commerce avec son téléphone, l’E-marketing, Community management, Entrepreneuriat numérique (créer, manager et lever des fonds pour son entreprise numérique) et Montage vidéo avec son téléphone <br>

                c. Le pack de souscription de cinq mille (5000) FCFA concède au Souscripteur le droit de se voir doter de Smartphone après avoir a priori encouragé l’adhésion de dix (10) autres personnes au pack souscrit au cours d’une période de deux (2) mois à compter de la validation de sa souscription. <br>
                d. Le pack de dix mille (10 000) FCFA octroie également un Smartphone à son souscrivant à la seule condition que celui-ci ait concouru à la souscription de cinq (5) autres personnes au pack souscrit dans une période de deux (2) mois à compter de l’acceptation du contrat de souscription. <br>
                e. Le mécanisme incitatif en vue de l’équipement en matériel de travail (Smartphone) fait l’objet d’un bonus donc indépendant de la formation qui est l’objet de ce contrat. <br>
                f. La fin de la formation après une période d’un (1) mois est sanctionnée par une attestations <br>
                Les déclarations, garanties et engagements précités, mis à la charge de l'Emetteur, demeureront pleinement en vigueur nonobstant l'exécution des modalités prévues au présent Contrat pour la mise en jeu des articles 6 et 7. <br>

                <h2>
                    5. LES OBLIGATIONS DU SOUSCRIPTEUR
                </h2>
                En contrepartie le Souscripteur a l’obligation d’honorer son engagement vis -à vis des clauses du présent contrat citées ci-dessus relatives aux modalités du programme de formation en observant les mesures suivantes : <br>

                <strong>
                    (A) Souscription et formation
                </strong> <br>
                a. Le paiement des frais d’inscription et de formation en moins de vingt-quatre (24) Heures après l’acceptation du contrat de souscription conformément à l’un des modes de paiement visés dans la section des modalités à l’article 4 du présent contrat <br>
                b. L’assiduité aux différentes séances de formation tout au long de la période de formation (huit ou seize heures de séances de cours selon le pack souscrit) <br>
                c. Le respect des consignes relatives au bon déroulement des séances (la ponctualité, la réactivité et autres attitudes que requiert l’apprentissage) <br>

                <strong>
                    (B) Equipement de travail
                </strong> <br>
                d. La communication autour du programme VOIA en vue de permettre son extension rapide. <br>
                e. Disposer d’un outil de travail(Smartphone) ou s’en procurer au cas échéant en remplissant les conditions de validation du bonus <br>
                f. Le respect du délai d’accessibilité au mécanisme d’équipement de travail (deux mois, à compter de la date de paiement) au cas où ce dernier ne dispose pas d’un téléphone au moment de la souscription <br>
                En bon apprenant, le souscripteur doit scrupuleusement observer l’ensemble de ces règles citées-ci-dessus pour un bon déroulement de sa formation. <br>

                <h2>
                    6. CLAUSE RESOLUTOIRE
                </h2>
                En cas de contestations, litiges ou différends sur l’interprétation ou l’exécution de la présente convention, les parties s’efforceront de parvenir à un règlement à l’amiable par voie de conciliation dans le délai de deux (2) mois. <br>
                Si néanmoins le désaccord persiste, le litige sera porté devant les juridictions compétentes auxquelles il est fait attribution de juridiction par les présentes. <br>

                <h2>
                    7. MODIFICATION DU CONTRAT
                </h2>
                Toute modification du présent contrat devra faire l’objet d’un avenant écrit et signé par les représentants des deux parties. <br>
